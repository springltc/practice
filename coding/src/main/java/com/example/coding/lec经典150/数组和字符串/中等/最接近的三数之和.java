/**
 * 给你一个长度为 n 的整数数组 nums 和 一个目标值 target。请你从 nums 中选出三个整数，使它们的和与 target 最接近。
 *
 * 返回这三个数的和。
 *
 * 假定每组输入只存在恰好一个解。
 */

import java.util.Arrays;

/**
 *示例 1：
 *
 * 输入：nums = [-1,2,1,-4], target = 1
 * 输出：2
 * 解释：与 target 最接近的和是 2 (-1 + 2 + 1 = 2) 。
 * 示例 2：
 *
 * 输入：nums = [0,0,0], target = 1
 * 输出：0
 */
public class 最接近的三数之和 {

    public static void main(String[] args) {
        int[] nums = {4, 0, 5, -5, 3, 3, 0, -4, -5};
        int target = -2;

        //-4
        // int[] nums = {-5,-5,-4,0,0,3,3,4,5,};

        int result = threeSumClosest(nums, target);
        System.out.println(result);
    }

    public static int threeSumClosest(int[] nums, int target) {
        Arrays.sort(nums);
        int result = nums[0] + nums[1] + nums[2];
        for (int i = 0; i < nums.length; i++) {
            int start = i + 1;
            int end = nums.length - 1;

            while (start < end) {
                int sum = nums[i] + nums[start] + nums[end];
                if (Math.abs(sum - target) < Math.abs(result - target)) {
                    result = sum;
                }
                if (sum > target) {
                    end--;
                } else if (sum < target) {
                    start++;
                } else {
                    return result;
                }
            }
        }

        return result;
    }

    public static int threeSumClosestFail(int[] nums, int target) {
        //失败 只排序会有问题 因为如果是负数的话 两边的数据可能很大 需要综合
//        int[] nums = {4,0,5,-5,3,3,0,-4,-5};
//        int target = -2;

        Arrays.sort(nums);

        int sum = nums[0] + nums[1] + nums[2];
        int sub = Math.abs(sum - target);
        int result = sum;
        for (int i = 3; i < nums.length; i++) {
            sum = sum + nums[i] - nums[i - 3];
            if ((Math.abs(sum - target)) < sub) {
                sub = Math.abs(sum - target);
                result = sum;
            }
        }

        return result;
    }

    public static int threeSumClosest2(int[] nums, int target) {
        Arrays.sort(nums);
        int ans = nums[0] + nums[1] + nums[2];
        for (int i = 0; i < nums.length; i++) {
            int start = i + 1, end = nums.length - 1;
            while (start < end) {
                int sum = nums[start] + nums[end] + nums[i];
                if (Math.abs(target - sum) < Math.abs(target - ans))
                    ans = sum;
                if (sum > target)
                    end--;
                else if (sum < target)
                    start++;
                else
                    return ans;
            }
        }
        return ans;
    }

}
